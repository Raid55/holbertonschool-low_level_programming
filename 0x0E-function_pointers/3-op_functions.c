/**
 * op_add - adds
 * @a: int a
 * @b: int b
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subs
 * @a: int a
 * @b: int b
 *
 * Return: sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mulls
 * @a: int a
 * @b: int b
 *
 * Return: sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divs
 * @a: int a
 * @b: int b
 *
 * Return: sum
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mods
 * @a: int a
 * @b: int b
 *
 * Return: sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
